								<h3>Recognition of Religious Marriages</h3>
								<p>
									Unless they comply with the English rules religious marriages may not be recognised by the  English &amp; Welsh Courts. Therefore a religiously  married couple may be considered as co-habiting by the English system and would not acquire the same rights as a legally recognised married couple.
								</p>
								<p>
									With a multicultural Briton there are many religious marriage ceremonies taking place in England and Wales and there are many couples living here having entered into a religious marriage outside abroad.
								</p>
								<p>
									However, some marriages are officially recognised in the UK, for example, where both a religious marriage and a civil marriage is conducted.
								</p>
								<p>
									A marriage which has been conducted overseas will also be recognised in the UK, provided it is recognised in accordance with the state laws of that country. However, difficulties arise where there is no marriage certificate available. In these  circumstances other evidence of the marriage such as photos, videos or  witnesses must be produced.
								</p>
								<h3>Religious divorces</h3>
								<p>
									When filing for a divorce however, it would be necessary to dissolve both the religious  marriage and dissolve the civil marriage.
								</p>
								<p>For example:
									<ol>
										<li>With a Muslim marriage it would be appropriate for there to be a &lsquo;talaq&rsquo; where the husband has the right to terminate the marriage without having a court ruling.&nbsp; In England, a &lsquo;talaq&rsquo; is not valid and so a formal English divorce procedure must also be followed to obtain the relevant financial claims.</li>
										<li>A Get is a Jewish Divorce document, which is required by Jewish couples who wish to obtain a divorce. Even if a civil divorce is granted by the English Court, many problems may occur if a Get is not obtained and so it is essential to have both the religious Get and a formal English divorce.</li>
									</ol>
								</p>
