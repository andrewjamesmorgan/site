						  	<h2>Separation</h2>
						  	<p>
						  		Upon the breakdown of a marriage parties will inevitably wish to lead separate and apart lives.
						  	</p>
						  	<p>
						  		It is not  compulsory to enter into divorce proceedings.&nbsp;  A divorce formally dissolves a marriage and the financial split of the  marriage assets can be regulated and enforced by the courts.
						  	</p>
						  	<h3>Some of the reasons you may chose to separate without divorce are:</h3>
						  	<p>
							  	<ol>
							  	  <li>Psychological or emotional reasons/li>
							  	  <li>religious reasons or</li>
							  	  <li>to remain separated for two years  and issue divorce by consent at the end of this period &ndash; a &ldquo;no fault&rdquo; <a href="Divorce.php" title="Divorce Law" name="Divorce Law" alt="Divorce Law">divorce</a></li>
							    </ol>
							</p>
						  	<p>
						  		You can continue to lead separate lives without ever formally recording what arrangements you have made relating to the financial settlement, if any.
						  	</p>
						  	<h3>Why have a Separation Agreement?</h3>
						  	<p>
						  		It is  advisable in many circumstances to have a record of your separation in writing to refer to over time&nbsp; to avoid disputes over what was agreed and why.
						  	</p>
						  	<p>
						  		This is particularly important when one of the parties develops a new relationship and the strength of trust between the estranged spouses begins to feel a little vulnerable.
						  	</p>
						  	<h3>The content of a Separation Agreement</h3>
						  	<p>They recite:
							  	<ol>
							  	  <li>your date of marriage</li>
							  	  <li>the number of children you have and their age and names</li>
							  	  <li>that the parties have chosen to lead separate lives from a specified date</li>
							  	  <li>whether or not the parties intend to issue divorce after two years separation, with consent of the other spouse</li>
							    </ol>
							</p>
						  	<p>
						  		They record:
							  	<ol>
							  	  <li>what has been agreed in relation to the sale or split of equity of the matrimonial home and other property</li>
							  	  <li>the division of savings, life policies, bank accounts and stocks and  shares</li>
							  	  <li>the division of the contents of the matrimonial home including antiques and items of monetary or sentimental value</li>
							  	  <li>whether there is to be a clean break against each other for future claims for income, capital, property or pension rights</li>
							  	  <li>both parties have sought or had the opportunity to seek legal advice</li>
							  	  <li>the intention to make the arrangements formal by way of a Consent Court Order within future divorce proceedings, if any.</li>
								</ol>
							</p>
						  	<h3>Important points to remember about Separation Agreements</h3>
						  	<p>
							  	<ol>
							  	  <li>unless you are on benefits, you can record child maintenance agreements without the CSA involvement and regulate  any increases in the level of maintenance within the agreement, e.g. linked to retail price index</li>
							  	  <li>the agreement is a personal arrangement between the married couple. It is not ratified by the court and therefore is based on trust and  confidence in each other that it will be adhered to</li>
							  	  <li>if a party breaches the agreement, the only way to enforce it is to issue divorce proceedings and ask the court to take account of the agreement and the circumstances in which it was entered into. The courts are not bound to uphold the agreement, but may put some weight on it</li>
							  	  <li>you cannot arrange a pension share or attachment within a Separation Agreement. It can only be ordered by the court following <a href="Divorce.php" title="Divorce Law" name="Divorce Law" alt="Divorce Law">divorce proceedings</a></li>
							  	  <li>though it is possible to record arrangements in respect of the care and contact of children, this is not  essential as there is an underlying principal that regardless of what is recorded, the <a href="Children.php">children&rsquo;s welfare</a> is of paramount importance and changing  circumstances requires a need for flexibility</li>
							  	  <li>you are still legally married and therefore each other&rsquo;s next of kin. It  is essential that you make a Will to ensure that the assets that you have retained following the separation are left to those who you wish to benefit and that they do not automatically fall to the estranged spouse</li>
							    </ol>
						    </p>
