							    <h2>Proceedings  relating to Children</h2>
							    <p>
							    	On separation or divorce both parties will naturally be concerned and have thought long and hard about the impact of the breakdown of the relationship on the children. Both will want to continue their bond and relationship with the children. Indeed this will be the case even more so now to reassure them that they are safe and loved.</p>
							    <p>
							    	The welfare  and interest of the children is of paramount importance. This is of course the case for all parents but is also the first principal of the courts in all Children Act matters.
						    	</p>
							    <h3>No Order Principal</h3>
							    <p>
							    	In the past, a divorce recorded which parent had &lsquo;custody&rsquo; of the children &ndash; or &lsquo;Residence&rsquo;. This often occasioned paper being waived about between the parties, in front of the children, showing authority and control over them.
						    	</p>
							    <p>
							    	Today&rsquo;s approach, is very different and further reviewed under the Children and Families Act 2014. There is a laissez-faire attitude. You are the parents. You know your children best and what arrangements are best for their day to day care and visiting absent parents. If matters are agreed there is no need to record this in writing or obtain sealed orders requiring the parties to abide by a rigid regime regardless of the need for flexibility with most arrangements.
							    </p>
							    <p>
							    	The only place the arrangements will be noted is the form relating to children file with a divorce petition and this only indicates the arrangements at the time the petition was issued and is not set in stone. Please refer to the <a href="Divorce.php" title="Family Law - Divorce" name="Family Law - Divorce" alt="Family Law - Divorce">Divorce page</a> for more details.
							    </p>
							    <h3>Negotiations</h3>
							    <p>
							    	Allwyn Sanger encourage parents to come to a sensible arrangement for the best interest of the children.
							    </p>
							    <p>
							    	Often Mediation can help parties keep a calm and constructive dialog to make arrangements. We can also help in the negotiating process and where appropriate record any agreements to act as a focus and established routine for all.
							    </p>
							    <p>
							    	Where it is not possible to come to your own arrangements an application to the Family courts may be issued under the Children and Family Act 2014 for a Child Arrangement Order. This can deal with all aspects of arrangements including:
								    <ul>
									  <li>Residence</li>
								      <li>Shared Residence</li>
								      <li>Contact &ndash; Direct or Indirect</li>
								      <li>Parental Responsibility</li>
								      <li>Specific Arrangements as to School Holidays/Weekends etc.</li>
								      <li>Specific Steps Order</li>
								      <li>Port Alert Order</li>
									</ul>	
								</p>
							    <h3>Issue Application</h3>
							    <p>
							    	Each party may make an application to the Court for the Order sought in form C100.
							    </p>
							    <h3>Conciliation Appointment</h3>
							    <p>
							    	This is the first Hearing. Both parties are required to attend Court and any children over the age of 9 years old may  attend Court at the Hearing. Prior to the hearing an officer from the Children and Family Court Advisory and Support Services (CAFCASS) or similar child arrangement facilitators in attendance at Court will speak to each of the children and each of the parents in a private room to ascertain their wishes. The (CAFCASS) Officer will then report to the Court and a temporary Order may be made accordingly. Directions are made by the District Judge to be reviewed at the next hearing. This is particularly the case where children are to spend time living with both parties.
							    </p>
							    <h3>Directions Appointment</h3>
							    <p>
							    	The Court often requires:
								    <ol>
								        <li>Each party to prepare a Statement in relation to the children to be filed atcourt and served on the opponent.</li>
								        <li>A CAFCASS Officer to carry out investigations including visiting the children discuss their wishes and talk to both parties in respect of their wishes for the children. A report by the CAFCASS Officer is then filed  at court and sent to both parties. This  can take 15-18 weeks to prepare.</li>
								    </ol>
								</p>
							    <h3>Further Directions</h3>
							    <p>
							    	The court may before listing the matter for a final hearing order a further directions appointment to bring the parties and CAFCASS together in an effort to agree arrangements and avoid the cost and inevitable stress of a final hearing.
							    </p>
							    <h3>Final Hearing</h3>
								<p>
									Where agreement has not been able to be reached with the guidance of the CAFCASS report and indications from the court then both parties and CAFCASS give oral evidence at a final  hearing at court. The Court will make an Order based on the evidence and its findings.
								</p>
							    <p>
							    	The  timescale for matters concerning children inevitably varies a great deal from  case to case and may also delay the divorce procedure.
							    </p>
