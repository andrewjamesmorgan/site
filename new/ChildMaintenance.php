							    <h2>Court Orders for the Maintenance for Children in Ancillary Relief</h2>
							    <p>
							    	The Child Support Agency must deal with all Child Maintenance against NRP (non-residential parent) unless there is a prior agreement in writing for child maintenance between the parties.
						    	</p>
							    <p>
							    	Child maintenance will also be under the courts jurisdiction if:
								    <ul type="disc">
									     <li>The parents have not yet separated or are still living at the same address</li>
									     <li>The matter involves step children</li>
									     <li>If the children are 18 or over</li>
									     <li>If a parent does not live in the UK or is employed overseas by a UK registered company or the Crown</li>
								    </ul>
								</p>
							    <p>
							    	In all other cases the Child Support Agency has jurisdiction to deal  with maintenance for children:
								    <ul type="disc">
									    <li>Under the age of 16 or</li>
									    <li>Under the age of 18 but still in full time education or</li>
									    <li>Under the age of 18 but satisfying certain conditions about being available for work and training.</li>
								    </ul>
							    </p>
							    <p>
							    	Maintenance is calculated at present based on a calculation taking  account of several factors:
								    <ul>
									    <li>the NRP&rsquo;s after-tax earnings</li>
									    <li>the number of nights the children spends with absent parent </li>
									    <li>how many children there are </li>
									    <li>The NRP will pay <br /><br /></li>
									    <ul>
									        <li>15% of their net weekly income for one child, </li>
									        <li>20% for two, and </li>
									        <li>25% for three or more.<br /><br /></li>
									    </ul>
									    <li>The CSA can make an assessment to a maximum of up to &pound;2,000 a week. A top-up order from the court may be obtained if the non resident parents wages are higher than this.</li>
