							    <h2>Parental  Responsibility</h2>
								<h3>Why is Parental Responsibility important?</h3>
								<p>
									Parental Responsibility is the legal definition which recognises parents &ldquo;rights, duties, powers, responsibilities and authority &hellip;. in relation to the child and his property&rdquo;. This is defined by the Children&rsquo;s Act 1989.
								</p>
								<p>
									Parental Responsibility gives power to the parent to make decisions in their child&rsquo;s life in all aspects such as which school the child attends, their medical treatment and their religion.
								</p>
								<h3>Who has Parental Responsibility of minor children?</h3>
								<p>
									The following category of people have Parental Responsibility:
									<ol>
									  <li>a mother automatically at birth</li>
									  <li>a father on marriage to the mother, either before or after the child&rsquo;s birth</li>
									  <li>a  father if he is named on the birth certificate</li>
									  <li>a  father if he obtains a Parental Responsibility Order or an agreement to a Parental  Responsibility Order with the mother</li>
									  <li>a  person with a Residence Order or appointed a guardian by the court</li>
									</ol>
								</p>
								<h3>Change of a Child&rsquo;s Name</h3>
								<p>
									All people who have Parental Responsibility must consent to the change of name even if the parents are separated, divorced or have further re-married or even where the father has no contact with the child.
								</p>
								<p>
									If the child is over 16 years of age however the people with Parental Responsibility are not required to consent and the child can arrange his or her own Change of Name Deed.
								</p>
								<p>
									Where only the mother has Parental Responsibility she has a right and power lawfully to change the name of her children. 
								</p>
								<p>
									Nevertheless the Court recognises a child&rsquo;s links with its father as being important and there  have been a number of cases which illustrate that fathers without Parental Responsibility have been able to reverse a change of their child&rsquo;s name. It is therefore good practice, if at all possible, to obtain the father&rsquo;s consent in any event.
								</p>
								<p>
									In any other situation a Court Order is required for permission to change the name, i.e. where the absent father with Parental Responsibility has no contact with the child. The Court&rsquo;s decision must be in the child&rsquo;s interest and account is taken of the degree of commitment by the absent father and the views of the child (where appropriate).
								</p>
								<h3>Recognition of a Change of Child's Name</h3>
								<p>
									You  should be aware that schools, GPs and other bodies cannot amend records in respect of child&rsquo;s name without enquiry to check consent has been given by all other people with Parental Responsibility or an Order of the Court.
								</p>
								<h3>What do you need to do to change a Childs Name?</h3>
								<p>
									You should get confirmation from all people with Parental Responsibility that they agree to consent to a change of their child&rsquo;s name. You should then provide their  full names and addresses. We can prepare the Change of Name Deed for execution  by you and arrange for it to be endorsed with the signed consent of all others  with Parental Responsibility.
								</p>
								<p>
									Where consent is not forthcoming you must apply to the court with a statement indicating why you believe it is in the child&rsquo;s interest to change their name and what endeavours you have made to get the person with Parental Responsibility&rsquo;s consent.
								</p>
