								<h2>Do you really need a Will?</h2>
								<p>
									A staggering  amount of people die intestate (without making a valid Will). It is vital to  have a correctly executed Will to ensure:
									<ul>
										<li><strong>No disputes amongst people you leave behind</strong><br />
										The peace of mind that you have correctly prepared your Will is highly satisfying. Your family and friends are spared possible disputes that can occur in speculating what your intentions and sometimes moral obligations had been immediately prior to your death. This obviously would be especially  distressing at a time when they are trying to cope with the loss of someone close to them.</li>
										<li><strong>Your intentions carried out</strong><br />
										If you want to have your wishes carried out after your death it is essential you make a Will to ensure your intentions are fulfilled and avoid your estate falling into  the wrong hands and against your desires.</li>
										<li><strong>Gifts to individuals</strong><br />
										Your Will can record who is to receive specific amounts of money and/or items. These individuals are specifically named in the Will and can be immediate family members, relatives, friends and charities.</li>
										<li><strong>Appointment of Guardianship of children</strong><br />
										Where there are children under eighteen unless provision is made for them in your Will by nominating legal guardians on your death Court orders can be made to decide who they live with. This is particularly of concern where only one parent has Parental Responsibility of the children such as in one-parent families or unmarried parents.</li>
										<li><strong>Tax</strong><br />
										Tax planning is essential if you wish to avoid Inheritance Tax, Capital Gains Tax and other taxes that may be payable by your estate on your death. There are therefore clear financial advantages of creating a valid Will with tax avoidance clauses.  However with the demise of a number of trusts as a result of The Finance Act 2006 previously used to avoid tax it is essential that Relevant Trusts are correctly worded to prevent them being inadvertently taxable trusts. Surviving spouses and civil partners can however, since October 2007, take advantage of any unused nil rate band of their deceased spouse/civil partner - <a href="TaxPlanning.php" title="Inheritance tax planning" name="Inheritance tax planning" alt="Inheritance tax planning"> see Tax Planning</a>.</li>
										<li><strong>Non UK Assets</strong><br />
										Probate rules and taxes in other countries vary from UK laws. It is often advisable to have a Will in each country in which you have assets to deal with the distribution in accordance with local law.</li>
									</ul>
								</p>
								<h3>Where there is no Will on death</h3>
								<p>
									If you die without having made a valid Will you are said to have died &lsquo;intestate&rsquo;. This means the Law decides how your Property and assets should be distributed. Your next of kin may only receive some of your estate, and your assets may be given to the wrong people.
									<ul>
										<li>Where a person dies leaving a spouse that spouse will not automatically get all the estate. This would only happen if the deceased&rsquo;s  assets were below a certain value, currently the first &pound;250,000 (correct as at 2013/14)</li>
										<ul>
											<li>Where there are children (no matter  how old they are) they may have a right to part of your estate.</li>
											<li>Where there are no children, parents, brothers and sisters may take a share. Inevitably the deceased&rsquo;s spouse may receive an amount which is clearly insufficient to live on comfortably.</li>
										</ul>
										<li>A Will referring to a gift to &ldquo;my children&rdquo; may include children the deceased did not intend to include, or even knew about. This class of beneficiaries includes:
										<ul>
											<li>children from a current or previous marriage or relationship</li>
											<li>children born to the deceased after he or she made a Will </li>
											<li>adopted children</li>
										</ul></li>
										<li>The surviving partner of a cohabiting couple ie unmarried, would not necessarily receive a benefit from the estate unless they can show they had been cohabiting for more than one year or were financially dependent on the deceased and would suffer hardship. This causes avoidable strain on the survivor which could be covered by a simple Will.</li>
										<li>It is also important for single people  to make a Will to ensure their estate is divided in the percentages they wish  ie amongst friends, relatives and charities of their choice. In default of a Will this group of people&rsquo;s  estate may simply pass to the Crown (ultimately the Treasury).</li>
									</ul>
								</p>
								<h3>Tax Issues</h3>
								<p>
									Property left to a spouse by the deceased is exempt of tax.
								</p>
								<p>
									Where the deceased&rsquo;s estate exceeds the Inheritance Tax Threshold at the time of death (currently &pound;325,000.00 &ndash; correct as at 2013/14) there may be Inheritance Tax to  pay on assets left to children and relatives. The amount of Inheritance Tax payable on assets over and above  &pound;325,000.00 is a staggering 40%!
								</p>
								<p>
									There is no Inheritance Tax to pay if your estate is worth less than (currently 2013/14) &pound;325,000 - <a href="TaxPlnning.php" title="Inheritance tax planing" name="Inheritance tax planning" alt="Inheritance tax planning"> see Tax Planning</a>.
								</p>
								<p>
									However, some simple tax planning and avoidance clauses can reduce the amount of tax that will have to be paid.
								</p>
								<p>
									You can make gifts throughout your lifetime to individuals and provided these payments are made seven years before death, they are exempt from tax.
								</p>
								<p>
									Legacies left to a registered charity are free of Inheritance Tax.
								</p>
