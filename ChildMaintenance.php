							    <h2>Court Orders for the Maintenance for Children in Ancillary Relief</h2>
							    <p>
							    	The Child Maintenance Service deal with all Child Maintenance against non residential parents (ie. Parent not living with child) unless there is prior agreement in writing for child maintenance between parents.
						    	</p>
							    <h3>The Child Maintenance Service can:</h3>
							    <ul>
								    <li>Try to find the other parent if you don’t know where they live</li>
									<li>Sort out disagreements about parentage
									Work out how much child maintenance should be paid</li>
									<li>Arrange for the ‘paying’ parent to pay child maintenance - the parent who doesn’t have main day-to-day care of the child</li>
									<li>Pass payments on to the ‘receiving’ parent - the parent who has main day-to-day care of the child</li>
									<li>Look at the payments again when changes in parents’ circumstances are reported</li>
									<li>Review the payment amount every year</li>
									<li>Take action if payments aren’t made</li>
							    </ul>
							    <h3>How the service works out child maintenance fees:</h3>
							    <ol>
							    	<li>They find out the paying parent’s yearly gross income (before tax and insurance) and check if they’re getting benefits</li>
									<li>Things that could change the gross income amount (eg pension payments or other children they support) are checked </li>
									<li>The Child Maintenance Service will take into account the number of children the paying parent has to pay child maintenance for</li>
									<li>Using information from the first 4 steps, the Child Maintenance Service decides the weekly child maintenance amount
										<table>
											<tr>
												<th width="200px">Number of children needing child maintenance</th>
												<th width="200px">Percentage of gross weekly income </th>
											</tr>
											<tr>
												<th>1</th>
												<td>12%</td>
											</tr>
											<tr>
												<th>2</th>
												<td>16%</td>
											</tr>
											<tr>
												<th>3 or more</th>
												<td>19%</td>
											</tr>
										</table>
									</li>
									<li>If “paying parent” cares for child/children in overnight care then a deduction to the child maintenance  costs are made, the level of which depends on the number (on average) of shared nights per week.</li>
							    </ol>
