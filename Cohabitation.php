						 	<h2>Co-Habitation</h2>
						  	<p>
						  		More and  more of us are choosing to live with our partners without marrying.
						  	</p>
						  	<h3>What is the legal position?</h3>
						  	<p>
							  	<ol>
							  	  <li>Over time, your financial affairs may become entangled.</li>
							  	  <li>Any assets in your name or gifted to you by your partner will remain yours on separation. Any assets in your partner&rsquo;s name or gifted to them, will remain theirs on separation. This includes all assets, savings, property, pensions and contents of a house.</li>
							  	  <li>One party may become finically  dependant on the other whilst contributing to the relationship in other ways,  such as maintaining the house or caring for children. However, should there be a separation, the  financial claims you would have had, had you been married, would not be available to you as a dependant spouse.</li>
							  	  <li>If you have a property in one person&rsquo;s name or a greater share in one person&rsquo;s name, it will remain  theirs. If then that person unexpectedly predeceases you without making a Will with a provision for you as a partner,  you, as the surviving partner, may be rendered homeless or have to sell or raise a larger mortgage to release the deceased&rsquo;s partner&rsquo;s share to his or her estate.</li>
							  	  <li>In these circumstances, claims by a surviving partner can be made under the Inheritance (Provision for Family and  Dependants) Act, but inevitably may cause delay, distress and the expense of litigation or negotiation.</li>
							    </ol>
							<p>
						  	<h3>Living together agreements</h3>
						  	<p>
						  		It is possible to record your financial intentions resulting from living together  by entering into a Co-habitation Agreement.
						  	</p>
					  		<ol>
						  	  <li>This is a personal agreement between the two of you and it is not ratified by the court.</li>
						  	  <li>It simply records what you and your partner intend to happen with assets in each other&rsquo;s names and the intention  when assets are purchased using joint or sole bank accounts and savings.</li>
						  	  <li>The agreement can also record what should happen to your finances and the content of a property in the unlikely event of a relationship breakdown. This acts as an insurance policy and does not have to be considered as encouraging fate or indicating mistrust.</li>
						    </ol>
						  	<h3>Remember</h3>
						  	<p>
						  		It is important to make a Will. If you are  not married to your partner they are not legally your next of kin.&nbsp; If you do not wish your partner to become  financially unstable at a traumatic time resulting from a loved one&rsquo;s death,  you should specify what is to be bequeathed to them in a Will.
					  		</p>	
