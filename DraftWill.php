								<h2>Basic details to make a Will</h2>
								<p>
									You should  list and consider the following:
									<ul>
										<li><strong>Appointment of Executors</strong> (this means the person or persons who are named in your Will to carry out your wishes in accordance with your directions) &ndash; note their full names and addresses. This can be a family member, a friend, a professional person (who may charge for their service) or a combination of these.</li>
										<li><strong>Specific  Gifts, including Charities</strong> &ndash; make a note of the full names and addresses of any person or body whom you wish to benefit with a specific amount or item.</li>
										<li><strong>Financial  Assets </strong>&ndash; consider the approximate value of these items, e.g. bank and building society accounts, stocks, shares and investments, national  savings, pension benefits, life assurance, premium bonds, unit trusts, business, money owed to you.</li>
										<li><strong>Property</strong> &ndash; consider the approximate value of these items, such as your home, any other property, your contents including furniture, clothes, antiques, jewellery, your car, and any other items of value.</li>
										<li><strong>Debts </strong>&ndash; consider the approximate value of any mortgage outstanding, other loans, overdrafts, hire purchase agreements, credit card debts, any other money owed.</li>
										<li><strong>Beneficiaries </strong>&ndash; consider who is to receive the balance of your estate after debts and specific gifts are discharged. List their full names and addresses.</li>
										<li><strong>Guardians</strong> &ndash; in the event of both parents predeceasing children under the age of 18 years  of age, the appointment of a Guardian (a relative or close friend) could be  invaluable and a clear expression of wish stating who should care for your children. Note their full names and  addresses and ask them if they are prepared to take up this voluntary  appointment. Provision can be left in the Will for the maintenance of the children to be taken from the estate.</li>
									</ul>
								</p>
								<h3>Ensuring your Will is valid</h3>
								<p>
									Your Will is only valid if it is correctly signed and witnessed. The witnesses must be two independent people (i.e. they should have nothing to gain from Will). This means witnesses should not be related to you or likely to benefit in any way from your Will.
								</p>
								<p>
									The witnesses must be assembled together and watch you sign the Will.You and each of them should watch the other  witness sign immediately after you. Special witnessing provisions are made for the visually impaired.
								</p>
								<p>
									For your ease of mind, we can act as your witnesses.
								</p>
								<p>
									We recommend that you retain the original Will for your safe keeping and let your Executors know of its whereabouts. You can also register your Will with the national will registration company called ‘Certainty’. There is a fee for this service and we can register your Will with them on your behalf if you wish.
								</p>
								<h3>DIY Wills.</h3>
								<p>
									Provided you have the basic rules of clearly setting out your wishes and executing the document correctly, you can draft your own Will. Obviously this means that there is a risk that the Will is not accurate or executed correctly and you will not have had the benefit of considering tax planning.
								</p>
								<p>
									You can also purchase pre-printed Wills from some major stationers with instructions on execution.
								</p>
								<p>
									For peace of mind, most people seek help from a Solicitor. Remember:
									<ol>
										<li>Making a Will does not need to be expensive</li>
										<li>It is easy to change your mind and your Will.</li>
									</ol>
								</p>
								<h3>Glossary of key words</h3>
								<p>
									Wills do not need to be drafted in complicated language. We aim to use simple English wherever possible. It is your Will and your intentions and if you cannot understand what your Will says you should not sign it.
								</p>
								<p>
									However legal documents do use specialist words which carry legal definitions and words used in Will drafting is no exception. We  have set out some words you may come across when instructing solicitors to prepare your Will to help you understand the legal terminology often used:
								</p>
								<p>
									<strong>Administrator</strong>
									</p>
								<p>
									Where you have not made a Will the person who collects and distributes your wealth and possessions is known as the Administrator. This is often a close relative who volunteers to administer the estate.
								</p>
								<p>
									<strong>Beneficiaries</strong>
								</p>
								<p>
									The person or persons who receive the benefits from your Will.
								</p>
								<p>
									<strong>Codicil</strong>
								</p>
								<p>
									A separate legal document making a change to an existing Will, which must be signed in the same way as a Will and though kept with the original Will should not be stapled, pinned or paperclipped to the original Will.
								</p>
								<p>
									<strong>Estate </strong>
								</p>
								<p>
									The total of what your wealth and possessions are on your death.
								</p>
								<p>
									<strong>Executors</strong>
								</p>
								<p>
									The  people named in a Will to look after your estate as per your direction in the  Will. This can be as few as one and as  many as four. They also act as your Trustees.
								</p>
								<p>
									<strong>Pecuniary Legacy</strong>
								</p>
								<p>
									The amount of money left in your Will.
								</p>
								<p>
									<strong>Specific Legacy</strong>
								</p>
								<p>
									A specific item/possession left in your Will.
								</p>
								<p>
									<strong>Intestate</strong>
								</p>
								<p>
									Someone who dies without having made a Will.
								</p>
								<p>
									<strong>Probate</strong>
								</p>
								<p>
									The formal procedure used to verify the Will, record the people administering it and giving authority to distribute your estate.
								</p>
								<p>
									<strong>Residue</strong>
								</p>
								<p>
									What is left of your estate after payment of debts, expenses and specific legacies etc.
								</p>
								<p>
									<strong>Testator/Testatrix</strong>
								</p>
								<p>
									The legal term used for the person making the Will. 
								</p>
								<h3>Revising your Will</h3>
								<p>
									We list some of the reasons why you may need to make a new Will.
									<ul>
										<li>A change in the tax laws - the  Finance Act 2006 has had a significant impact on existing Wills set up in trust for tax avoidance.</li>
										<li>If you marry after making an existing Will</li>
										<li>If you divorce and your former spouse is named in your Will.</li>
										<li>If you separate from your spouse.</li>
										<li>If you change your name or anyone mentioned in your Will changes theirs</li>
										<li>If an Executor dies or becomes unsuitable to act due to age or ill health</li>
										<li>If a beneficiary dies</li>
										<li>If you have left a property which you subsequently sell or which changes character</li>
										<li>The birth of a child or grandchild </li>
										<li>Changes in the value of your estate or your financial circumstances</li>
										<li>Change of mind of who is to benefit and how.</li>
									</ul>
