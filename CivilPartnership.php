						  	<h2>The Civil  Partnership Act 2004</h2>
						  	<p>
						  		The Civil  Partnership Act 2004 came into force on 5 December 2004. It gave couples of the same sex a right to register themselves as civil partners. It provides recognition of their commitment to each other and gives rights  and responsibilities in line with married couples.
						  	</p>
						  	<p>
						  		There have been a tremendous amount of registered civil partners since the Act came into force and much higher than the government&rsquo;s initial prediction.
						  	</p>
						  	<p>
						  		More recent high profile civil partnerships have been Elton John and Matt Lucas.
						  	</p>
						  	<h3>Who can register?</h3>
						  	<p>
							  	<ul>
							  	  <li>Two people of the same sex</li>
							  	  <li>Who are not either already a civil partner or married and</li>
							  	  <li>Who are over 16 (in England and Wales parental consent in necessary if a civil partner is under 18) and</li>
							  	  <li>Who are not within prohibited  degrees of relationship (child, adoptive child, parent, grandparent, sibling  etc.)</li>
							    </ul>
						    </p>
						  	<h3>Procedure</h3>
						  	<p>There are different procedures for different circumstances</p>
						  	<p>The standard procedure is the most common procedure that will apply where the following steps will require to take place:</p>
						  	<p>
							  	<ul>
							  	  <li>A notice of the planned partnership must be given to the registration authority which must contain the names of  both civil partners and the location of the registration.</li>
							  	  <li>The notice must contain a written  declaration that:</li>
							  	  <ul>
								     <li>There is no impediment to the creation of a civil partnership and</li>
							         <li>That both parties have lived in England or Wales for at least seven days before the giving of the notice.</li>
							      </ul>
							  	  <li>A waiting period of 15 days follows, beginning with the day that the notice is recorded, during which time the  proposed civil partnership must be published by the relevant registration  authority.</li>
							    </ul>
						    </p>
						  	<p>
						  		Special procedure would apply to reflect the diverse situations that may prevent people  from using the main standard procedure, such as obtaining medical certificates.
						  	</p>
						  	<h3>The ceremony of the partnership</h3>
						  	<p>
						  		Similar to a marriage, the partnership must take place in the location specified in the notice and in the presence of each other, the Registrar and two witnesses. Each partner signs the civil partnership schedule. The witnesses and the Registrar must also sign this schedule in the partners&rsquo; presence.
						  	</p>
						  	<p>
						  		It is important to note that no religious service may take place during the ceremony  of the partnership.
						  	</p>
						  	<h3>The legal impact of entering into a civil partnership</h3>
						  	<p>
						  		Upon entering into a civil partnership you acquire rights as well as responsibilities which are similar to the Matrimonial Causes Act where there is a marriage. As a  consequence, the Civil Partnership Act has had a huge knock-on effect on other related legislation such as the law relating to:
						  	</p>
						  	<p>
							  	<ol>
							  	  <li>Children</li>
							  	  <li>The treatment of tax, including Inheritance Tax Relief on death</li>
							  	  <li>Recognition of overseas registered civil partnerships</li>
							  	  <li>Pension rights, both relating to benefits payable to a surviving partner on death and pension sharing on a  breakdown of a relationship</li>
							  	  <li>The death of one partner and inheritance of the surviving partner as next of kin</li>
							  	  <li>Employment Law and discrimination rights at work</li>
							  	  <li>Protection from domestic violence</li>
							    </ol>
							</p>
						  	<h3>Ending the civil partnership</h3>
						  	<p>
						  		A registered civil partnership can be ended only by dissolution, annulment, or death.
						  	</p>
						  	<p>
						  		Similar to marriages the partnership can only be dissolved where the partnership has been registered for at least one year and must be on the basis that it has broken down  irretrievably.
					  		</p>
						  	<p>
						  		The grounds for the dissolution are similar to divorce proceedings, i.e. you would need to particularise that the dissolution is on the basis of either unreasonable behaviour of the other party or the parties have separated for a period of time set by the legislation.
						  	</p>
